package com.scxh.java.classs.genericity;

/**
 * x = 10、y = 10 
 * x = 12.88、y = 129.65 
 * x = "东经180度"、y = "北纬210度"
 * 
 * @author viktor
 * 
 */
public class Point {
	private Object x;
	private Object y;

	public Object getX() {
		return x;
	}

	public void setX(Object x) {
		this.x = x;
	}

	public Object getY() {
		return y;
	}

	public void setY(Object y) {
		this.y = y;
	}
}
